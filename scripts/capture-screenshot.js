/**
 * MagMark 2.0 - Screenshot Capture Script
 */

const fs = require('fs');
const path = require('path');

console.log('📸 MagMark 2.0 - Visual Test Screenshot Generator\n');

const htmlPath = path.join(__dirname, 'visual-test.html');
const markdownPath = path.join(__dirname, '..', 'tests', 'samples', 'test-article.md');
const outputDir = path.join(__dirname, '..', 'screenshots');

if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
}

console.log('Step 1: Reading source files...\n');

try {
    const htmlContent = fs.readFileSync(htmlPath, 'utf8');
    console.log('✅ Visual Test HTML loaded (' + htmlContent.length + ' bytes)');
    
    const refPath = path.join(outputDir, 'reference-render.html');
    fs.writeFileSync(refPath, htmlContent);
    console.log('   Saved to: ' + refPath + '\n');
} catch (error) {
    console.error('❌ Failed to read HTML file:', error.message);
    process.exit(1);
}

try {
    const mdContent = fs.readFileSync(markdownPath, 'utf8');
    console.log('✅ Markdown sample loaded (' + mdContent.length + ' bytes)');
    
    const mdRefPath = path.join(outputDir, 'source-article.md');
    fs.writeFileSync(mdRefPath, mdContent);
    console.log('   Saved to: ' + mdRefPath + '\n');
} catch (error) {
    console.error('❌ Failed to read Markdown file:', error.message);
    process.exit(1);
}

// Generate simple test report
var report = '# 📸 MagMark 2.0 Visual Test Report\n\n';
report += '**Generated**: ' + new Date().toISOString() + '\n\n';
report += '---\n\n';
report += '## Test Files\n\n';
report += '| File | Size | Status |\n';
report += '|------|------|--------|\n';
report += '| Source Markdown | ' + fs.statSync(markdownPath).size + ' bytes | OK |\n';
report += '| Visual Test HTML | ' + fs.statSync(htmlPath).size + ' bytes | OK |\n';
report += '| Reference Render | Generated | OK |\n';
report += '| Source Archive | Generated | OK |\n';
report += '\n---\n\n';
report += '## Next Steps\n\n';
report += '1. Open screenshots/reference-render.html in browser\n';
report += '2. Take screenshots of the rendered page\n';
report += '3. Save images to this directory\n';
report += '4. Update this report with filenames\n';
report += '\n---\n';
report += '**Report generated by MagMark 2.0**';

const reportPath = path.join(outputDir, 'visual-test-report.md');
fs.writeFileSync(reportPath, report);
console.log('Step 2: Generated test report\n');

console.log('Test report saved to: ' + reportPath + '\n');

console.log('============================================================');
console.log('📸 SCREENSHOT GENERATION COMPLETE');
console.log('============================================================');
console.log('\nTo complete visual testing:');
console.log('1. Open: screenshots/reference-render.html');
console.log('2. Use browser DevTools (F12)');
console.log('3. Take screenshots:');
console.log('   - Desktop view (full page)');
console.log('   - Mobile view (device mode)');
console.log('   - Print preview (Ctrl+P)');
console.log('\nSave images to: screenshots/\n');
console.log('============================================================');

process.exit(0);
